/**
 * Generate a project-specific context file that tells AI coding assistants
 * what's configured in this project. Like Stripe's project-context.md.
 */
export function writeProjectContext(
  projectDir: string,
  manifest: Manifest,
  envVars: Record<string, boolean>,
): void {
  const dir = join(projectDir, ".whop");
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });

  const lines: string[] = [
    "# Project Context",
    "",
    "Auto-generated by create-whop-kit. Helps AI coding assistants understand",
    "your project. Regenerated by `whop-kit status`. Do not edit manually.",
    "",
    "## Project",
    "",
    `- **Framework:** ${manifest.framework}`,
    `- **App Type:** ${manifest.appType}`,
    `- **Database:** ${manifest.database}`,
    `- **Template Version:** ${manifest.templateVersion}`,
    `- **Created:** ${manifest.createdAt}`,
    "",
    "## Configuration Status",
    "",
  ];

  const checks = [
    ["DATABASE_URL", "Database", true],
    ["NEXT_PUBLIC_WHOP_APP_ID", "Whop App ID", true],
    ["WHOP_API_KEY", "Whop API Key", true],
    ["WHOP_WEBHOOK_SECRET", "Webhook Secret", true],
    ["EMAIL_PROVIDER", "Email", false],
    ["ANALYTICS_PROVIDER", "Analytics", false],
  ] as const;

  for (const [key, label, required] of checks) {
    const set = envVars[key] ?? false;
    const icon = set ? "✓" : required ? "✗" : "○";
    lines.push(`- ${icon} ${label} (\`${key}\`)`);
  }

  if (manifest.features.length > 0) {
    lines.push("");
    lines.push("## Installed Features");
    lines.push("");
    for (const feature of manifest.features) {
      lines.push(`- ${feature}`);
    }
  }

  lines.push("");
  lines.push("## Key Files");
  lines.push("");

  if (manifest.framework === "nextjs") {
    lines.push("- `lib/auth.ts` — session management (whop-kit/auth)");
    lines.push("- `lib/adapters/next.ts` — Next.js cookie adapter");
    lines.push("- `lib/adapters/prisma.ts` — Prisma DB + config adapters");
    lines.push("- `app/api/webhooks/whop/route.ts` — webhook handler");
    lines.push("- `app/api/auth/` — OAuth login/callback/logout");
    lines.push("- `db/schema.prisma` — database schema");
  } else if (manifest.framework === "astro") {
    lines.push("- `src/lib/auth.ts` — session management (whop-kit/auth)");
    lines.push("- `src/lib/adapters/astro.ts` — Astro cookie adapter");
    lines.push("- `src/lib/adapters/prisma.ts` — Prisma DB adapter");
    lines.push("- `src/pages/api/webhooks/whop.ts` — webhook handler");
    lines.push("- `src/pages/api/auth/` — OAuth login/callback/logout");
  }

  lines.push("");
  lines.push("## CLI Commands");
  lines.push("");
  lines.push("```bash");
  lines.push("npx whop-kit status        # check project health");
  lines.push("npx whop-kit add email     # add email provider");
  lines.push("npx whop-kit add analytics # add analytics");
  lines.push("npx whop-kit catalog       # list available services");
  lines.push("npx whop-kit open whop     # open Whop dashboard");
  lines.push("npx whop-kit upgrade       # update whop-kit");
  lines.push("```");
  lines.push("");

  writeFileSync(join(dir, "project-context.md"), lines.join("\n"));
}

