/**
 * Write a provider-specific skill when a feature is added.
 * Only for providers that don't have an official agent-skills repo.
 */
export function writeFeatureSkill(
  projectDir: string,
  featureKey: string,
  provider: string,
): void {
  const dir = join(projectDir, ".agents", "skills", "whop-kit-features");
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });

  const content = generateFeatureSkill(featureKey, provider);
  if (content) {
    writeFileSync(join(dir, `${featureKey}-${provider}.md`), content);
  }
}

