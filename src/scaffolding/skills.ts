import { writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join } from "node:path";
import { exec } from "../utils/exec.js";
import type { Manifest } from "./manifest.js";

// ---------------------------------------------------------------------------
// Official skill repos — installed via `npx skills add`
// ---------------------------------------------------------------------------

interface SkillSource {
  repo: string;
  skill: string;
}

/** Map of database/feature provider → official agent skills to install */
const PROVIDER_SKILLS: Record<string, SkillSource[]> = {
  neon: [
    { repo: "https://github.com/neondatabase/agent-skills", skill: "neon-postgres" },
    { repo: "https://github.com/neondatabase/ai-rules", skill: "neon-serverless" },
  ],
  supabase: [
    { repo: "https://github.com/supabase/agent-skills", skill: "supabase-postgres-best-practices" },
  ],
  "prisma-postgres": [
    // Prisma skill is typically bundled in the template already
  ],
};

/**
 * Install official provider skills via `npx skills add`.
 * Runs in the project directory. Non-blocking — failures are warnings, not errors.
 */
export function installProviderSkills(projectDir: string, provider: string): void {
  const skills = PROVIDER_SKILLS[provider];
  if (!skills || skills.length === 0) return;

  for (const { repo, skill } of skills) {
    const result = exec(
      `npx -y skills add ${repo} --skill ${skill}`,
      projectDir,
    );
    if (!result.success) {
      // Non-fatal — skills are nice-to-have
      console.log(`  (Could not install ${skill} skill — you can add it later)`);
    }
  }
}

